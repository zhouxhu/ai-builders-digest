#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

// Enhanced news fetcher for follow-builders-digest
// This script fetches news using Tavily API and formats it properly

const WORK_DIR = "/Users/zx/.openclaw/workspace/follow-builders-digest";
const DATE = new Date().toISOString().split('T')[0];
const NEWS_FILE = path.join(WORK_DIR, `${DATE}-news.md`);

// Set TAVILY_API_KEY if not set
const TAVILY_API_KEY = process.env.TAVILY_API_KEY || "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";

// Queries for different news categories
const queries = {
    international: "world news international breaking news global events",
    china: "China news Chinese politics economy technology domestic news", 
    tech: "AI artificial intelligence technology innovation startups venture capital"
};

// Function to fetch news from Tavily API
async function fetchNews(query, maxResults = 8) {
    try {
        const response = await fetch('https://api.tavily.com/search', {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
            },
            body: JSON.stringify({
                api_key: TAVILY_API_KEY,
                query: query,
                topic: "news",
                days: 1,
                max_results: maxResults,
                include_answer: false,
                include_raw_content: false,
            })
        });

        if (!response.ok) {
            throw new Error(`Tavily API error: ${response.status}`);
        }

        const data = await response.json();
        return data.results || [];
    } catch (error) {
        console.error(`Error fetching news for query "${query}":`, error.message);
        return [];
    }
}

// Function to format news results
function formatNewsResults(results, category) {
    const categoryEmojis = {
        international: "🌍",
        china: "🇨🇳", 
        tech: "💼"
    };

    const categoryNames = {
        international: "国际新闻",
        china: "国内新闻",
        tech: "财经科技"
    };

    if (!results || results.length === 0) {
        return `## ${categoryEmojis[category]} ${categoryNames[category]}\n\n*暂无新闻数据*\n\n`;
    }

    let content = `## ${categoryEmojis[category]} ${categoryNames[category]}\n\n`;
    
    results.forEach((result, index) => {
        const title = result.title || "Untitled";
        const url = result.url || "#";
        const relevance = result.score ? ` (relevance: ${(result.score * 100).toFixed(0)}%)` : "";
        
        content += `- **${title}${relevance}**\n`;
        content += `  <${url}>\n\n`;
    });

    return content;
}

// Function to create news file
async function createNewsFile() {
    console.log(`📰 Creating news file for ${DATE}...`);

    // Fetch news for all categories
    const [internationalResults, chinaResults, techResults] = await Promise.all([
        fetchNews(queries.international),
        fetchNews(queries.china),
        fetchNews(queries.tech)
    ]);

    // Count articles
    const internationalCount = internationalResults.length;
    const chinaCount = chinaResults.length;
    const techCount = techResults.length;
    const totalCount = internationalCount + chinaCount + techCount;

    // Format content
    const content = `---
layout: default
title: "新闻速递 — ${DATE}"
date: ${DATE}
---

# 📰 新闻速递 — ${DATE}

---

${formatNewsResults(internationalResults, 'international')}---
${formatNewsResults(chinaResults, 'china')}---
${formatNewsResults(techResults, 'tech')}---

## 📊 今日数据

**精选文章：${totalCount} 篇**

- 🌍 国际: ${internationalCount} 篇
- 🇨🇳 国内: ${chinaCount} 篇
- 💼 科技: ${techCount} 篇

---

*Generated by OpenClaw News Aggregator - ${new Date().toLocaleString('zh-CN')}*

---

## ⚠️ 数据来源

所有新闻数据均来自 Tavily API 实时搜索结果。
`;

    // Write file
    fs.writeFileSync(NEWS_FILE, content);
    console.log(`✅ News file created: ${NEWS_FILE}`);
    console.log(`📊 Total articles fetched: ${totalCount}`);
}

// Main execution
async function main() {
    try {
        await createNewsFile();
        console.log("✅ News fetch completed successfully");
        process.exit(0);
    } catch (error) {
        console.error("❌ News fetch failed:", error.message);
        process.exit(1);
    }
}

main();